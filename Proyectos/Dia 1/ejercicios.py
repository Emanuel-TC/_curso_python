# Cadenas con el uso de caracteres especiales

# Usamos la diagonas invertida para poder usar caracteres especiales
print("Hola, me llamo \"Emanuel\" ")
# Para saltos línes usamos \n
print("Hola, mundo, uso la \"\\n\" para hacer un \nsalto de línea")
# Para tabular usamos \t
print("Usamos el \"\\t\" para hacer una \ttabulación")


'''
Crea un código que imprima en pantalla la siguiente expresión (pero usando print una sola vez):
Línea 1
Línea 2
Línea 3
'''
print("Línea 1\nLínea 2\nLínea 3")

'''
Crea un código que imprima en pantalla la siguiente expresión.
A	B	C
D	E	F
G	H	I
'''

print("A\tB\tC\nD\tE\tF\nG\tH\tI")

'''
Crea un código que imprima en pantalla la siguiente expresión:

Barra Normal: /
Barra Invertida: \
'''
